USE ABP_PADEL;

DROP TABLE IF EXISTS ROL;
CREATE TABLE ROL (
	id int(15) NOT NULL AUTO_INCREMENT, 
	nombre varchar(25) NOT NULL DEFAULT 'Deportista', 

	PRIMARY KEY (id)
);


DROP TABLE IF EXISTS FUNCIONALIDAD;
CREATE TABLE FUNCIONALIDAD(
	id int(15) NOT NULL AUTO_INCREMENT, 
	nombre varchar(25) NOT NULL, 
	descripcion varchar(200) NOT NULL,

	PRIMARY KEY (id)
);


DROP TABLE IF EXISTS ACCION;
CREATE TABLE ACCION(
	id int(15) NOT NULL AUTO_INCREMENT, 
	nombre varchar(25) NOT NULL, 
	descripcion varchar(200) NOT NULL,

	PRIMARY KEY (id)
);


DROP TABLE IF EXISTS ROL_PERMISOS;
CREATE TABLE ROL_PERMISOS(
	ROL_id int(15) NOT NULL, 
	FUNCIONALIDAD_id int(15) NOT NULL, 
	ACCION_id int(15) NOT NULL, 

	PRIMARY KEY (ROL_id, FUNCIONALIDAD_id, ACCION_id),
	FOREIGN KEY (ROL_id) REFERENCES ROL (id) ,
	FOREIGN KEY (FUNCIONALIDAD_id) REFERENCES FUNCIONALIDAD (id),
	FOREIGN KEY (ACCION_id) REFERENCES ACCION (id)
);


DROP TABLE IF EXISTS USUARIO;
CREATE TABLE USUARIO (
	login varchar(25) NOT NULL, 
	password varchar(40) NOT NULL, 
	nombre varchar(25) NOT NULL, 
	apellidos varchar(25) NOT NULL, 
	genero enum('Masculino', 'Femenino') NOT NULL, 
	email varchar(60) NOT NULL UNIQUE, 
	ROL_id int(255) NOT NULL, 
	
	PRIMARY KEY (login),
	FOREIGN KEY (ROL_id) REFERENCES ROL (id) ON DELETE CASCADE
);


DROP TABLE IF EXISTS PISTA;
CREATE TABLE PISTA (
	nombre varchar(25) NOT NULL, 
	tipo varchar(40) NOT NULL, 
	
	PRIMARY KEY (nombre)
);


DROP TABLE IF EXISTS HORARIO;
CREATE TABLE HORARIO (
	id int(255) NOT NULL AUTO_INCREMENT, 
	hora_inicio time NOT NULL UNIQUE, 
	hora_fin time NOT NULL UNIQUE, 
	
	PRIMARY KEY (id)
);


DROP TABLE IF EXISTS CATEGORIA;
CREATE TABLE CATEGORIA (
	id int(255) NOT NULL AUTO_INCREMENT, 
	nivel int(1) NOT NULL, 
	genero varchar(25) NOT NULL,

	PRIMARY KEY (id)
);


DROP TABLE IF EXISTS CAMPEONATO;
CREATE TABLE CAMPEONATO (
	id int(255) NOT NULL AUTO_INCREMENT, 
	nombre varchar(50) NOT NULL, 
	fecha date NOT NULL, 
	USUARIO_admin_login varchar(25) NOT NULL, 

	PRIMARY KEY (id),
	FOREIGN KEY (USUARIO_admin_login) REFERENCES USUARIO (login)  ON DELETE CASCADE
);


DROP TABLE IF EXISTS PAREJA;
CREATE TABLE PAREJA (
	id int(255) NOT NULL AUTO_INCREMENT, 
	capitan varchar(25) NOT NULL, 
	jugador_2 varchar(25) NOT NULL, 
	
	PRIMARY KEY (id),
	FOREIGN KEY (capitan) REFERENCES USUARIO (login) ON DELETE CASCADE,
	FOREIGN KEY (jugador_2) REFERENCES USUARIO (login) ON DELETE CASCADE
);


DROP TABLE IF EXISTS RESERVA;
CREATE TABLE RESERVA (
	id int(255) NOT NULL AUTO_INCREMENT, 
	fecha date NOT NULL, 
	USUARIO_login varchar(25) NOT NULL, 
	HORARIO_id int(255) NOT NULL, 
	PISTA_nombre varchar(25) NOT NULL, 
	
	PRIMARY KEY (id),
	FOREIGN KEY (USUARIO_login) REFERENCES USUARIO (login) ON DELETE CASCADE,
	FOREIGN KEY (HORARIO_id) REFERENCES HORARIO (id) ON DELETE CASCADE,
	FOREIGN KEY (PISTA_nombre) REFERENCES PISTA (nombre)  ON DELETE CASCADE
);


DROP TABLE IF EXISTS PARTIDO;
CREATE TABLE PARTIDO (
	id int(255) NOT NULL AUTO_INCREMENT, 
	fecha date NOT NULL, 
	inscritos int(1) DEFAULT 0 NOT NULL, 
	HORARIO_id int(255) NOT NULL, 
	RESERVA_id int(255), 
	USUARIO_login varchar(25) NOT NULL, 
	
	PRIMARY KEY (id),
	FOREIGN KEY (HORARIO_id) REFERENCES HORARIO (id) ON DELETE CASCADE,
	FOREIGN KEY (RESERVA_id) REFERENCES RESERVA (id) ON DELETE CASCADE,
	FOREIGN KEY (USUARIO_login) REFERENCES USUARIO (login)  ON DELETE CASCADE
);


DROP TABLE IF EXISTS USUARIO_PARTIDO;
CREATE TABLE USUARIO_PARTIDO (
	USUARIO_login varchar(25) NOT NULL, 
	PARTIDO_id int(255) NOT NULL, 
	
	PRIMARY KEY (USUARIO_login, PARTIDO_id),
	FOREIGN KEY (USUARIO_login) REFERENCES USUARIO (login) ON DELETE CASCADE,
	FOREIGN KEY (PARTIDO_id) REFERENCES PARTIDO (id) ON DELETE CASCADE
);


DROP TABLE IF EXISTS GRUPO;
CREATE TABLE GRUPO (
	id int(255) NOT NULL AUTO_INCREMENT, 
	nombre varchar(20) NOT NULL, 
	CAMPEONATO_id int(255) NOT NULL, 
	CATEGORIA_id int(255) NOT NULL, 

	PRIMARY KEY (id),
	FOREIGN KEY (CAMPEONATO_id) REFERENCES CAMPEONATO (id)  ON DELETE CASCADE,
	FOREIGN KEY (CATEGORIA_id) REFERENCES CATEGORIA (id) ON DELETE CASCADE
);


DROP TABLE IF EXISTS INSCRIPCION;
CREATE TABLE INSCRIPCION (
	id int(255) NOT NULL AUTO_INCREMENT, 
	fecha date NOT NULL, 
	PAREJA_id int(255) NOT NULL, 
	CATEGORIA_CAMPEONATO_id int(10) NOT NULL,
	GRUPO_id int(255),

	PRIMARY KEY (id),
	FOREIGN KEY (PAREJA_id) REFERENCES PAREJA (id) ON DELETE CASCADE,
	FOREIGN KEY (CATEGORIA_CAMPEONATO_id) REFERENCES CATEGORIA_CAMPEONATO (id) ON DELETE CASCADE,
	FOREIGN KEY (GRUPO_id) REFERENCES GRUPO (id) ON DELETE CASCADE
);


DROP TABLE IF EXISTS ENFRENTAMIENTO;
CREATE TABLE ENFRENTAMIENTO (
	id int(255) NOT NULL AUTO_INCREMENT, 
	resultado varchar(30) DEFAULT NULL, 
	PAREJA_1 int(255) NOT NULL, 
	PAREJA_2 int(255) NOT NULL, 
	GRUPO_id int(255) NOT NULL, 
	RESERVA_id int(255) DEFAULT NULL, 

	PRIMARY KEY (id),
	FOREIGN KEY (GRUPO_id) REFERENCES GRUPO (id) ON DELETE CASCADE,
	FOREIGN KEY (PAREJA_1) REFERENCES PAREJA (id) ON DELETE CASCADE,
	FOREIGN KEY (PAREJA_2) REFERENCES PAREJA (id) ON DELETE CASCADE,
	FOREIGN KEY (RESERVA_id) REFERENCES RESERVA (id) ON DELETE CASCADE
);


DROP TABLE IF EXISTS HUECO_DISPONIBLE;
CREATE TABLE HUECO_DISPONIBLE (
	id int(255) NOT NULL AUTO_INCREMENT,  
	fecha date NOT NULL, 
	HORARIO_id int(255) NOT NULL, 
	PAREJA_id int(255) NOT NULL, 
	ENFRENTAMIENTO_id int(255) NOT NULL, 

	PRIMARY KEY (id),
	FOREIGN KEY (PAREJA_id) REFERENCES PAREJA (id) ON DELETE CASCADE,
	FOREIGN KEY (HORARIO_id) REFERENCES HORARIO (id) ON DELETE CASCADE,
	FOREIGN KEY (ENFRENTAMIENTO_id) REFERENCES ENFRENTAMIENTO (id) ON DELETE CASCADE
);


DROP TABLE IF EXISTS CATEGORIA_CAMPEONATO;
CREATE TABLE CATEGORIA_CAMPEONATO (
	id int(10) NOT NULL AUTO_INCREMENT,
	CAMPEONATO_id int(255) NOT NULL, 
	CATEGORIA_id int(255) NOT NULL, 

	PRIMARY KEY (id),
	FOREIGN KEY (CAMPEONATO_id) REFERENCES CAMPEONATO (id) ON DELETE CASCADE,
	FOREIGN KEY (CATEGORIA_id) REFERENCES CATEGORIA (id) ON DELETE CASCADE
);


DROP TABLE IF EXISTS CLASICICACION;
CREATE TABLE CLASIFICACION (
	id int(255) NOT NULL AUTO_INCREMENT, 
	puntos int(255) NOT NULL, 
	PAREJA_id int(255) NOT NULL, 
	GRUPO_id int(255) NOT NULL, 

	PRIMARY KEY (id),
	FOREIGN KEY (PAREJA_id) REFERENCES PAREJA (id) ON DELETE CASCADE,
	FOREIGN KEY (GRUPO_id) REFERENCES GRUPO (id) ON DELETE CASCADE
